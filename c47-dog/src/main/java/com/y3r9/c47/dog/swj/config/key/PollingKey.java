package com.y3r9.c47.dog.swj.config.key;

/**
 * The Enum PollingKey.
 * 
 * @version 1.4
 * @since project 3.0
 */
public enum PollingKey {

    /**
     * The idle park mode.
     * 
     * @since 1.2
     */
    idleParkMode,

    /** The idle retry count. */
    idleRetryCount,

    /** The idle sleep nanosecond. */
    idleSleepNano,

    /** The cache size. */
    cacheSize,

    /** The concurrent builder enabled. */
    concurrentBuilderEnabled,

    /**
     * The over size park mode.
     * 
     * @since 1.2
     * */
    overSizeParkMode,

    /** The over size retry count. */
    overSizeRetryCount,

    /** The over size blocking nanosecond. */
    overSizeBlockingNano,

    /** The push to process size. */
    pushToProcessSize,

    /** The push to process millisecond. */
    pushToProcessMilli,

    /**
     * The break process size.
     * 
     * @since 1.3
     */
    breakProcessSize,

    /**
     * The partitionScheduler.
     *
     * @since 1.4
     */
    partitionScheduler,

    /** The briefSortSize. */
    briefSortSize,

    /** The scatterSelector. */
    scatterSelector,

    /** The swingSelector. */
    swingSelector,
}
